# Append
list1 = ["hi", "hello"]
print("Append")
list1.append ("bonjour")
print(list1)
print("")

# Extend
print("Extend")
list1 = [1,2,3]
list2 = [4,5]
print(f"{list1} and {list2}")
list1.extend(list2)
print(list1)
print("")

#Delete
print ("Delete")
list1 = [1,2,3,4,5]
print(list1)
list1.pop()
print(list1)